x = "Hello World" # Assigning string to variable
print("Length: ", len(x)) # len(var) gives length of variable
print(x[0]) # var[index] gives value at that index
print(x[2:5]) # var[st index, end index] gives string between those index
print(x.upper()) # var.upper() changes string to upper case
print(x.lower())# var.lower()changes string to lower case
print(x.replace("H","J")) # replace the element
print(x[-1::-1]) # reverses the string